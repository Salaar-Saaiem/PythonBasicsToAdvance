'''
Print is function use to display the message,
u can use single '', or double"" quotations
u have to use braces and there is no need to add ; this at the end
'''


print('Hello Saaiem Salaar!')

# You can also use double quotes
print("Welcome to Python programming!") 

# You can also use triple quotes for multi-line strings
print('''This is a multi-line string.
It can span multiple lines.''')

# You can also use escape characters
print("He said, \"Hello!\"")  # Using double quotes inside a string
print('It\'s a nice day!')  # Using single quote inside a string
# You can also use raw strings to ignore escape characters
print(r"C:\Users\Saaiem\Documents")  # Raw string to ignore backslashes


# u can also use print to display variables
name = "Saaiem" 
print(f"Hello, {name}!")  # Using f-string to include variable in the output
#f-string is a way to format strings in Python, it allows you to include variables and expressions inside a string by using curly braces {}.
