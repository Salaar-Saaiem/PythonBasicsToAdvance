'''
Comparision operators are used to compare two values, 
    it will only provide boolean output like true/false, yes/no etc
'''

a=10
b=20

print(a==b) #checks whether two values are equal or not
print(a!=b) #checks whether two values are not equal or not
print(a>b) #checks whether a is larger than b or not
print(a<b) #checks whether a is smaller than b or not
print(a>=b) #checks whether a is larger than equal to b or not
print(a<=b) #checks whether a is smaller than equal to b or not
